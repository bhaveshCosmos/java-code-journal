package datatypes;
public class integers {
    public static void main(String[] args) {
        // Integer Types
        byte num_byte = 127; // 1 byte
        short num_short = 32767; // 2 byte
        int num_int = 99999; // 4 byte
        long num_long = 218938199; // 8 byte

        System.out.printf("Byte: %d\nShort: %d\nInt: %d\nLong: %d", num_byte, num_short, num_int, num_long);
    }
    
}
